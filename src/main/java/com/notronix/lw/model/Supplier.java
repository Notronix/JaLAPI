package com.notronix.lw.model;

public class Supplier
{
    private String pkSupplierID;
    private String SupplierName;
    private String ContactName;
    private String Address;
    private String AlternativeAddress;
    private String City;
    private String Region;
    private String Country;
    private String PostCode;
    private String TelephoneNumber;
    private String SecondaryTelNumber;
    private String FaxNumber;
    private String Email;
    private String WebPage;

    public String getPkSupplierID()
    {
        return pkSupplierID;
    }

    public void setPkSupplierID(String pkSupplierID)
    {
        this.pkSupplierID = pkSupplierID;
    }

    public String getSupplierName()
    {
        return SupplierName;
    }

    public void setSupplierName(String supplierName)
    {
        SupplierName = supplierName;
    }

    public String getContactName()
    {
        return ContactName;
    }

    public void setContactName(String contactName)
    {
        ContactName = contactName;
    }

    public String getAddress()
    {
        return Address;
    }

    public void setAddress(String address)
    {
        Address = address;
    }

    public String getAlternativeAddress()
    {
        return AlternativeAddress;
    }

    public void setAlternativeAddress(String alternativeAddress)
    {
        AlternativeAddress = alternativeAddress;
    }

    public String getCity()
    {
        return City;
    }

    public void setCity(String city)
    {
        City = city;
    }

    public String getRegion()
    {
        return Region;
    }

    public void setRegion(String region)
    {
        Region = region;
    }

    public String getCountry()
    {
        return Country;
    }

    public void setCountry(String country)
    {
        Country = country;
    }

    public String getPostCode()
    {
        return PostCode;
    }

    public void setPostCode(String postCode)
    {
        PostCode = postCode;
    }

    public String getTelephoneNumber()
    {
        return TelephoneNumber;
    }

    public void setTelephoneNumber(String telephoneNumber)
    {
        TelephoneNumber = telephoneNumber;
    }

    public String getSecondaryTelNumber()
    {
        return SecondaryTelNumber;
    }

    public void setSecondaryTelNumber(String secondaryTelNumber)
    {
        SecondaryTelNumber = secondaryTelNumber;
    }

    public String getFaxNumber()
    {
        return FaxNumber;
    }

    public void setFaxNumber(String faxNumber)
    {
        FaxNumber = faxNumber;
    }

    public String getEmail()
    {
        return Email;
    }

    public void setEmail(String email)
    {
        Email = email;
    }

    public String getWebPage()
    {
        return WebPage;
    }

    public void setWebPage(String webPage)
    {
        WebPage = webPage;
    }
}
